
function toggleSeeMore1() {
    if(document.getElementById("textarea1").style.display == 'inline') {
        document.getElementById("textarea1").style.display = 'none';
        document.getElementById("seeMore1").innerHTML = 'إظهار المزيد';

    }
    else {
        document.getElementById("textarea1").style.display = 'none';
        document.getElementById("textarea1").style.display = 'inline';
        document.getElementById("seeMore1").innerHTML = 'إخفاء';        
    }
}


